// Generates the static Quran verse data bundled under lib/quran-text/data so the
// app needs ZERO external network calls at runtime to display verses. Previously
// the reader fetched text from api.alquran.cloud on the client, which is blocked
// from Iran without a VPN.
//
// Sources (must be reachable when running this script; the OUTPUT is fully static):
//   - Arabic (Uthmani): api.quran.com  -> text_uthmani (Tanzil-style Uthmani, matches
//     the previous `quran-uthmani` edition and renders with the bundled QuranTaha font)
//   - Persian (Fooladvand): fawazahmed0/quran-api `fas-mohammadmahdifo`
//     (Mohammad Mahdi Fooladvand, Tanzil source, matches the previous `fa.fooladvand`)
//
// Both sources use the standard 6236-ayah numbering, so they align 1:1 by (surah, verse).
//
// Re-run with:  node scripts/generate-quran-text.mjs
import { mkdir, writeFile, rm } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const OUT_DIR = join(ROOT, 'lib', 'quran-text', 'data')
const LOADERS_FILE = join(ROOT, 'lib', 'quran-text', 'loaders.generated.ts')

const ARABIC_URL = 'https://api.quran.com/api/v4/quran/verses/uthmani'
const FOOLADVAND_URL =
  'https://cdn.jsdelivr.net/gh/fawazahmed0/quran-api@1/editions/fas-mohammadmahdifo.min.json'

const TOTAL_VERSES = 6236

async function fetchJson(url) {
  const res = await fetch(url)
  if (!res.ok) throw new Error(`Fetch failed (${res.status}) for ${url}`)
  return res.json()
}

async function main() {
  console.log('Fetching Arabic (Uthmani) from api.quran.com ...')
  const arabic = await fetchJson(ARABIC_URL)
  console.log('Fetching Fooladvand (Persian) from fawazahmed0/quran-api ...')
  const persian = await fetchJson(FOOLADVAND_URL)

  // surah -> { [verse]: { a: arabic, t: translation } }
  const bySurah = {}
  for (const v of arabic.verses) {
    const [s, a] = v.verse_key.split(':').map(Number)
    ;(bySurah[s] ??= {})[a] = { a: v.text_uthmani.trim(), t: '' }
  }
  for (const v of persian.quran) {
    const entry = bySurah[v.chapter]?.[v.verse]
    if (entry) entry.t = v.text.trim()
  }

  let total = 0
  let missingText = 0
  for (let s = 1; s <= 114; s++) {
    const verses = bySurah[s]
    if (!verses) throw new Error(`Missing surah ${s}`)
    for (const key of Object.keys(verses)) {
      total++
      if (!verses[key].a || !verses[key].t) missingText++
    }
  }
  if (total !== TOTAL_VERSES)
    throw new Error(`Expected ${TOTAL_VERSES} verses, got ${total}`)
  if (missingText)
    throw new Error(`${missingText} verses are missing arabic or translation`)

  await rm(OUT_DIR, { recursive: true, force: true })
  await mkdir(OUT_DIR, { recursive: true })
  for (let s = 1; s <= 114; s++) {
    await writeFile(join(OUT_DIR, `${s}.json`), JSON.stringify(bySurah[s]))
  }

  const loaderLines = []
  for (let s = 1; s <= 114; s++) {
    loaderLines.push(`  ${s}: () => import('./data/${s}.json'),`)
  }
  const loaders = `// AUTO-GENERATED by scripts/generate-quran-text.mjs — do not edit by hand.
// Each surah is a separate dynamic import so the bundler code-splits it into its
// own chunk, fetched from our own origin only when that surah is first read.
export const surahLoaders: Record<
  number,
  () => Promise<{ default: unknown }>
> = {
${loaderLines.join('\n')}
}
`
  await writeFile(LOADERS_FILE, loaders)

  console.log(`Wrote 114 surah files (${total} verses) to ${OUT_DIR}`)
  console.log(`Wrote ${LOADERS_FILE}`)
}

main().catch(err => {
  console.error(err)
  process.exit(1)
})
